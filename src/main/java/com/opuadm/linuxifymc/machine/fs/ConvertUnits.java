// Converting Units here (As a part of the FileSystem)
package com.opuadm.linuxifymc.machine.fs;

public class ConvertUnits {
    public static final long KB = 1000; // 1KB in Bytes
    public static final long KIB = 1024; // 1KiB in Bytes
    public static final long MB = 1000 * KB; // 1MB in Bytes
    public static final long MIB = 1024 * KIB; // 1MiB in Bytes
    public static final long GB = 1000 * MB; // 1GB in Bytes
    public static final long GIB = 1024 * MIB; // 1GiB in Bytes
}
